CREATE TABLE IF NOT EXISTS tblSECOrder (
  `OrderId` INT NOT NULL AUTO_INCREMENT,
  `ProductId` INT NOT NULL,
  `UserId` INT NOT NULL,
  `Phone` VARCHAR(16) NOT NULL,
  `Address` VARCHAR(64) NOT NULL,
  `Count` INT NOT NULL,
  PRIMARY KEY (`OrderId`));

